# ---------------------------------------------------------------------------
# Dependency Verifier
# ---------------------------------------------------------------------------

class DependencyVerifier:
    """
    Verifies that AI-generated dependencies are real and safe.

    Responsibilities:
    - Extract package names from import statements and requirement files
    - Check packages against known registries
    - Detect hallucinated / slopsquatted packages
    - Flag suspicious patterns (typosquatting, new packages)
    - Maintain org-level approved dependency catalog
    """

    def __init__(self) -> None:
        self._approved_packages: set[str] = set()
        self._blocked_packages: set[str] = set(KNOWN_HALLUCINATED)
        self._scan_history: list[VerificationReport] = []

    # -- Configuration ------------------------------------------------------

    def add_approved_package(self, name: str) -> None:
        """Add a package to the organization's approved list."""
        self._approved_packages.add(name.lower().strip())

    def add_blocked_package(self, name: str) -> None:
        """Add a package to the blocked list."""
        self._blocked_packages.add(name.lower().strip())

    # -- Extraction ---------------------------------------------------------

    def extract_python_imports(self, code: str) -> list[str]:
        """Extract package names from Python import statements."""
        packages: set[str] = set()

        # import foo / import foo.bar
        for m in re.finditer(r"^\s*import\s+([\w.]+)", code, re.MULTILINE):
            top_level = m.group(1).split(".")[0]
            packages.add(top_level)

        # from foo import bar / from foo.bar import baz
        for m in re.finditer(r"^\s*from\s+([\w.]+)\s+import", code, re.MULTILINE):
            top_level = m.group(1).split(".")[0]
            packages.add(top_level)

        # Filter out stdlib modules
        return [p for p in sorted(packages) if p not in _PYTHON_STDLIB]

    def extract_npm_packages(self, code: str) -> list[str]:
        """Extract package names from JavaScript/TypeScript imports."""
        packages: set[str] = set()

        # import ... from 'package' / import ... from "package"
        for m in re.finditer(
            r"""(?:import|require)\s*\(?[^'"]*['"]([^'"./][^'"]*)['"]\)?""",
            code,
        ):
            pkg = m.group(1)
            # Handle scoped packages (@scope/name)
            if pkg.startswith("@"):
                parts = pkg.split("/")
                if len(parts) >= 2:
                    packages.add(f"{parts[0]}/{parts[1]}")
            else:
                packages.add(pkg.split("/")[0])

        return sorted(packages)

    def extract_requirements(self, content: str) -> list[str]:
        """Extract package names from requirements.txt format."""
        packages: list[str] = []
        for raw_line in content.strip().splitlines():
            stripped = raw_line.strip()
            if not stripped or stripped.startswith("#") or stripped.startswith("-"):
                continue
            # Remove version specifiers
            name = re.split(r"[>=<!~\[]", stripped)[0].strip()
            if name:
                packages.append(name.lower())
        return packages

    # -- Verification -------------------------------------------------------

    def verify_package(self, name: str, registry: str = "auto") -> DependencyCheck:
        """Verify a single package name."""
        name_lower = name.lower().strip()

        # Check approved list first
        if name_lower in self._approved_packages:
            return DependencyCheck(
                package_name=name,
                registry=registry,
                status=VerificationStatus.ALLOWLISTED,
                risk_level=RiskLevel.SAFE,
                reasons=["Package is in organization's approved list"],
            )

        # Check blocked list
        if name_lower in self._blocked_packages:
            return DependencyCheck(
                package_name=name,
                registry=registry,
                status=VerificationStatus.HALLUCINATED,
                risk_level=RiskLevel.CRITICAL,
                reasons=["Package is in known hallucinated/blocked list"],
            )

        # Determine registry
        if registry == "auto":
            registry = self._guess_registry(name_lower)

        # Check against known packages
        known = self._check_known_registry(name_lower, registry)
        if known is not None:
            return known

        # Check suspicious patterns
        suspicious_reasons = self._check_suspicious_patterns(name_lower)
        if suspicious_reasons:
            return DependencyCheck(
                package_name=name,
                registry=registry,
                status=VerificationStatus.SUSPICIOUS,
                risk_level=RiskLevel.MEDIUM,
                reasons=suspicious_reasons,
                suggested_alternative=self._find_similar_package(name_lower, registry),
            )

        # Unknown — can't verify without network access
        return DependencyCheck(
            package_name=name,
            registry=registry,
            status=VerificationStatus.UNKNOWN,
            risk_level=RiskLevel.LOW,
            reasons=["Package not in local registry cache; manual verification needed"],
        )

    def verify_code(self, code: str, language: str = "python") -> VerificationReport:
        """Verify all dependencies in a code block."""
        if language == "python":
            packages = self.extract_python_imports(code)
            registry = "pypi"
        elif language in ("javascript", "typescript"):
            packages = self.extract_npm_packages(code)
            registry = "npm"
        else:
            packages = []
            registry = "unknown"

        now = datetime.now(UTC).isoformat()
        checks = [self.verify_package(p, registry) for p in packages]

        verified = sum(1 for c in checks if c.status == VerificationStatus.VERIFIED)
        hallucinated = sum(1 for c in checks if c.status == VerificationStatus.HALLUCINATED)
        suspicious = sum(1 for c in checks if c.status == VerificationStatus.SUSPICIOUS)
        blocked = hallucinated > 0

        report = VerificationReport(
            checked_at=now,
            total_packages=len(packages),
            verified=verified,
            hallucinated=hallucinated,
            suspicious=suspicious,
            checks=checks,
            blocked=blocked,
        )
        self._scan_history.append(report)
        return report

    def verify_requirements(self, content: str) -> VerificationReport:
        """Verify a requirements.txt file."""
        packages = self.extract_requirements(content)
        now = datetime.now(UTC).isoformat()
        checks = [self.verify_package(p, "pypi") for p in packages]

        verified = sum(1 for c in checks if c.status == VerificationStatus.VERIFIED)
        hallucinated = sum(1 for c in checks if c.status == VerificationStatus.HALLUCINATED)
        suspicious = sum(1 for c in checks if c.status == VerificationStatus.SUSPICIOUS)

        report = VerificationReport(
            checked_at=now,
            total_packages=len(packages),
            verified=verified,
            hallucinated=hallucinated,
            suspicious=suspicious,
            checks=checks,
            blocked=hallucinated > 0,
        )
        self._scan_history.append(report)
        return report

    # -- Stats --------------------------------------------------------------

    def get_stats(self) -> dict[str, Any]:
        """Return aggregate scan statistics."""
        total_scans = len(self._scan_history)
        total_packages = sum(r.total_packages for r in self._scan_history)
        total_hallucinated = sum(r.hallucinated for r in self._scan_history)
        total_blocked = sum(1 for r in self._scan_history if r.blocked)

        return {
            "total_scans": total_scans,
            "total_packages_checked": total_packages,
            "total_hallucinated": total_hallucinated,
            "total_blocked_reports": total_blocked,
            "approved_packages_count": len(self._approved_packages),
            "blocked_packages_count": len(self._blocked_packages),
        }

    @property
    def scan_history(self) -> list[VerificationReport]:
        return list(self._scan_history)

    # -- Internal -----------------------------------------------------------

    def _guess_registry(self, name: str) -> str:
        if name in KNOWN_PYPI_PACKAGES:
            return "pypi"
        if name in KNOWN_NPM_PACKAGES:
            return "npm"
        if name.startswith("@"):
            return "npm"
        return "pypi"

    def _check_known_registry(
        self, name: str, registry: str
    ) -> DependencyCheck | None:
        known_set = (
            KNOWN_PYPI_PACKAGES if registry == "pypi" else KNOWN_NPM_PACKAGES
        )
        if name in known_set:
            return DependencyCheck(
                package_name=name,
                registry=registry,
                status=VerificationStatus.VERIFIED,
                risk_level=RiskLevel.SAFE,
                reasons=[f"Package found in known {registry} registry"],
            )
        return None

    def _check_suspicious_patterns(self, name: str) -> list[str]:
        reasons: list[str] = []
        for pattern, reason in SUSPICIOUS_PATTERNS:
            if re.match(pattern, name):
                reasons.append(reason)
        return reasons

    def _find_similar_package(self, name: str, registry: str) -> str | None:
        """Find the most similar real package name (simple Levenshtein)."""
        known_set = (
            KNOWN_PYPI_PACKAGES if registry == "pypi" else KNOWN_NPM_PACKAGES
        )
        best: str | None = None
        best_dist = float("inf")

        for known in known_set:
            d = _levenshtein(name, known)
            if d < best_dist and d <= 3:  # max 3 edits
                best_dist = d
                best = known

        return best


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _levenshtein(s1: str, s2: str) -> int:
    """Simple Levenshtein distance (no external deps)."""
    if len(s1) < len(s2):
        return _levenshtein(s2, s1)
    if len(s2) == 0:
        return len(s1)

    prev_row = list(range(len(s2) + 1))
    for i, c1 in enumerate(s1):
        curr_row = [i + 1]
        for j, c2 in enumerate(s2):
            insertions = prev_row[j + 1] + 1
            deletions = curr_row[j] + 1
            substitutions = prev_row[j] + (c1 != c2)
            curr_row.append(min(insertions, deletions, substitutions))
        prev_row = curr_row

    return prev_row[-1]


