"""
Hallucinated Dependency Detection (Anti-Slopsquatting).

Automatically verifies that every package/library referenced in AI-generated
code actually exists in its claimed registry (PyPI, npm).  Cross-references
suggested dependencies against known-good package lists, checks for
suspicious patterns, and blocks hallucinated names that could be registered
by attackers.

Industry context (2025-2026):
- ~20% of LLM-recommended packages do not exist in any public registry
- 43% of hallucinated names recur predictably across prompts
- Slopsquatting attacks register fake AI-hallucinated package names
"""

from __future__ import annotations

import re
from dataclasses import dataclass, field
from datetime import UTC, datetime
from enum import StrEnum
from typing import Any

# ---------------------------------------------------------------------------
# Data types
# ---------------------------------------------------------------------------

class VerificationStatus(StrEnum):
    VERIFIED = "verified"          # package confirmed to exist
    HALLUCINATED = "hallucinated"  # package does not exist
    SUSPICIOUS = "suspicious"      # exists but has red flags
    UNKNOWN = "unknown"            # unable to verify
    ALLOWLISTED = "allowlisted"    # in org approved list


class RiskLevel(StrEnum):
    SAFE = "safe"
    LOW = "low"
    MEDIUM = "medium"
    HIGH = "high"
    CRITICAL = "critical"


@dataclass
class DependencyCheck:
    """Result of checking a single dependency."""
    package_name: str
    registry: str  # "pypi" | "npm" | "unknown"
    status: VerificationStatus
    risk_level: RiskLevel
    reasons: list[str] = field(default_factory=list)
    suggested_alternative: str | None = None

    def to_dict(self) -> dict[str, Any]:
        return {
            "package_name": self.package_name,
            "registry": self.registry,
            "status": self.status.value,
            "risk_level": self.risk_level.value,
            "reasons": self.reasons,
            "suggested_alternative": self.suggested_alternative,
        }


@dataclass
class VerificationReport:
    """Aggregate report for all dependencies in a code block."""
    checked_at: str
    total_packages: int = 0
    verified: int = 0
    hallucinated: int = 0
    suspicious: int = 0
    checks: list[DependencyCheck] = field(default_factory=list)
    blocked: bool = False

    def to_dict(self) -> dict[str, Any]:
        return {
            "checked_at": self.checked_at,
            "total_packages": self.total_packages,
            "verified": self.verified,
            "hallucinated": self.hallucinated,
            "suspicious": self.suspicious,
            "blocked": self.blocked,
            "checks": [c.to_dict() for c in self.checks],
        }


